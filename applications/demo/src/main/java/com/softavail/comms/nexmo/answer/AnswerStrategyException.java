package com.softavail.comms.nexmo.answer;

public class AnswerStrategyException extends Exception {

  public AnswerStrategyException() {
    super();
  }

  public AnswerStrategyException(String message) {
    super(message);
  }

  public AnswerStrategyException(Throwable cause) {
    super(cause);
  }

  public AnswerStrategyException(String message, Throwable cause) {
    super(message, cause);
  }

}
